                            <div>
                              <p style="text-align:justify">Summer is around the corner and exciting times are here again. It is indeed great to get out in the field, with friends and family and enjoy the days in the outdoors. TCCC is pleased to announce the Season 2019 open in the second week of May 2019.</p>
                              <br/>
                             <p style="text-align:justify">As discussed in our pre-season meetings, we continue to strive towards improving the quality of the experience of playing with TCCC for all participants. This year we continue to work with the Toronto City Umpires and Scorers Association (http://www.tcuandsa.org ). We also will be using the CricHq app for online and live scoring. CricHq has made a huge change in how we score and provide real time information to participating teams and enthusiasts.Season 2019 begins with a round of practice games so that teams have an opportunity to get acclimatized to the conditions and outdoors after a long winter hiatus. This season we have increased the number of matches that each team plays toeighteen (18) followed by the play offs for the top four teams. This gives the teams more opportunities for fun, participation and time outdoors promoting active living.</p>
                           <br/>
                           <p style="text-align:justify">The enthusiasm amongst the teams as evident in the Captains and Manager meetings and is pleasantly infectious. There were several ideas proposed and we have tried to accommodate as many of them as practicable. Changes to balls (new suppliers), Umpiring with seasoned T&D level 2 and above skills,online scoring using CricHq and prescriptive guidance for the playoffs are some of the inputs that the TCCC board has considered for this season. As usual we have a few on our back burner (size of the grounds, facilities at the grounds, condition of the grounds etc.) which we are working with the City of Toronto to continue to improve.</p>
                           <br/>
                              <p style="text-align:justify">I would like to thank the TCCC Board for their continued support in helping pull together this amazing event. I would like to take this opportunity to wish those teams well that have moved on to other divisionsand welcome the new teams to the league.</p>
                             <br/>
                             <p style="text-align:justify">Looking forward to an exciting season with lots of runs, tons and off course miles of smiles along the way. In the end, it is the camaraderie that we foster matters while we enjoy the gentlemen’s game of cricket.</p>
                              
                              <p style="margin-top:10px;">Have a wonderful season in 2019.</p>
